namespace Cake.HockeyApp
{
    /// <summary>
    /// Api parameter option to set the version manditory.
    /// Default NotMandatory
    /// </summary>
    public enum MandatoryOption
    {
        /// <summary>
        /// Set the version not mandatory
        /// </summary>
        NotMandatory = 0,

        /// <summary>
        /// Set the version as mandatory
        /// </summary>
        Mandatory = 1
    }
}